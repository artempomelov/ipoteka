function getBankBySlug(slug) {
  return BANKS.find((b) => b.slug === slug) || null;
}

// Инициалы для фолбэка, если логотип не загрузился.
function bankInitials(name) {
  const words = name.replace(/\(.*?\)/g, "").trim().split(/[\s-]+/).filter(Boolean);
  if (words.length === 1) return words[0].slice(0, 2).toUpperCase();
  return (words[0][0] + words[1][0]).toUpperCase();
}

// Универсальный бейдж-логотип. src — путь к картинке (или null → сразу инициалы).
// При ошибке загрузки показываем инициалы на фирменном цвете.
function logoBadge(name, color, src, extraClass) {
  const cls = "bank-avatar" + (extraClass ? " " + extraClass : "");
  const init = bankInitials(name);
  if (!src) return `<span class="${cls} no-logo" style="--accent:${color}">${init}</span>`;
  return `<span class="${cls}" style="--accent:${color}">
    <img src="${src}" alt="${name}" loading="lazy"
      onerror="var p=this.parentNode;p.classList.add('no-logo');p.textContent='${init}';">
  </span>`;
}

function bankLogoHtml(bank, extraClass) {
  return logoBadge(bank.name, bank.color, `assets/logos/${bank.slug}.${bank.logoExt || "png"}`, extraClass);
}

function devLogoHtml(dev, extraClass) {
  const src = dev.logo ? `assets/logos/dev-${dev.slug}.${dev.logoExt || "png"}` : null;
  return logoBadge(dev.name, dev.color, src, extraClass);
}

